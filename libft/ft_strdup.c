#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	int		length;
	char	*copy;

	i = 0;
	length = ft_strlen(s);
	if (!(copy = (char *)malloc(sizeof(*copy) * (length + 1))))
		return (NULL);
	while (s[i] != '\0')
	{
		copy[i] = s[i];
		i++;
	}
	copy[i] = '\0';
	return (copy);
}
